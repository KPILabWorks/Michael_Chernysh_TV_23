client = OpenAI(
    api_key=OPEN_AI_API_KEY
)

async def send_user_text(user_text: str, mood: str) -> str:
    prompt = start_prompts[mood]
    completion = client.chat.completions.create(
        model="gpt-3.5-turbo",
        messages=[{"role": "developer", "content": prompt},{"role": "user", "content": user_text}]
    )
    response = completion.choices[0].message.content
    return response


